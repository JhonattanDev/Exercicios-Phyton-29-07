# Para explicar o programa
print("Insira as duas notas abaixo para calcular a média delas! \n")

# Inputs para o usuário inserir as 2 notas
nota1 = int(input("Digite a primeira nota: "))
nota2 = int(input("Digite a segunda nota: "))

# Cálculo da média das 2 notas
media = (nota1 + nota2) / 2

# Para pular uma linha
print("")

# Imprimir/mostrar o resultado do programa
print("A média das duas notas é {}!".format(media))
